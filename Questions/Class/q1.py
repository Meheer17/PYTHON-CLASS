#1 write a program to enter two integers and then perform all the arithmetic  operations on them
num1 = int(input("Enter the first value: "))
num2 = int(input("Enter the second value: "))
print("Arithmetic  Operations:")
print("The sum of the two numbers: \t",num1 + num2)
print("The diff of the numbers: \t", num1 - num2)
print("The product of the two numbers: \t", num1 * num2)
print("The division of the two numbers: \t", num1 / num2)
print("The quotient of the numbers: \t", num1 // num2)
print("The remainder of the two numbers: \t", num1 % num2)
print("The power of the two numbers: \t", num1 ** num2)

